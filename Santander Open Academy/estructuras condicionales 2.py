# La estructura IF-ELSE nos permite que si la condicion if es falsa ejecute un else

edad=15

if edad >=18:
   print ("Eres mayor de edad")

else:
   print("Eres menor de edad")

# En este caso si la edad es mayor o igual que 18 se ejecutara el bloque de codigo de if y si se determina lo contrario se ejecutara un else imprimiendo ("Eres menor de edad")