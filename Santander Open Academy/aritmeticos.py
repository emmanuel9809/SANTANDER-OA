#operadores Aritmeticos.
a=10
b=3
# Estas son operaciones basicas aritmeticas
suma= (a+b)
print(suma)

resta=(a-b)
print(resta)

multiplicacion=(a*b)
print(multiplicacion)

div_decimal=(a/b)
print(div_decimal)

div_sin_decimal=(a//b)
print(div_sin_decimal)

exponenciacion=(a**b)
print(exponenciacion)

# Estas son operadores de comparacion
igual_a=(a==b)
print(igual_a)

diferente=(a!=b)
print(diferente)

mayor_que=(a>b)
print(diferente)

menor_que=(a<b)
print(menor_que)

mayor_igual=(a>=b)
print(mayor_igual)

menor_igual=(a<=b)
print(menor_igual)

#operadores logicos and, or, not

resultado_and=(a>5 and b<5)
print(resultado_and)

